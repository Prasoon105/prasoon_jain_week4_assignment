package Arraylist;

class Student {
    private String name;
    private int id;

    public Student(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    public void displayStudent() {
        System.out.println("ID: " + id + ", Name: " + name);
    }
}



